#include <stdio.h>
/*
        *****
       ******
      *******
     ********
    *********
     ******** --> 5 --> 1
      ******* --> 6 --> 2
       ****** --> 7 --> 3
        ***** --> 5 --> 4

        *
       *
      *
     *
    *********
     *
      *
       *
        *

*/
int main()
{
    int i, j, k = 1;
    for (i = 0; i < 9; i++)
    {
        for (j = 0; j < 9; j++)
        {
            if (i == 4 || (i <= 3 && j == (9 / 2) - i) || (i >= 5 && j == k))
            {
                printf("*");
            }
            else
            {
                printf(" ");
            }
        }
        if (i >= 5)
        {
            k++;
        }
        printf("\n");
    }

    return 0;
}